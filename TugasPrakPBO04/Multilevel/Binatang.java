public class Binatang {
    // Protected variable 'name'
    protected String name;

    // Constructor untuk inisialisasi nama binatang
    public Binatang(String name) {
        this.name = name;
    }

    // Method bersuara() di class Binatang
    public void bersuara() {
        System.out.println(name + " bersuara: Mbeek");
    }
}
