public class Karyawan {
    // Protected variable 'nama'
    protected String nama;

    // Constructor untuk inisialisasi nama karyawan
    public Karyawan(String nama) {
        this.nama = nama;
    }

    // Method kerja() di class Karyawan
    public void kerja() {
        System.out.println(nama + " kerja: kerja");
    }
}
